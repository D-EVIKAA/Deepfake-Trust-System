"""
AI Watermark / Logo Detection  (v2 — enhanced)
===============================================

Detects AI-generator watermarks using five layered methods:

  Method 1 — Full-image pytesseract OCR with 8 preprocessing variants
              (plain, high-contrast, inverted, sharpened, OTSU, adaptive,
               CLAHE-equalised, bilateral-filtered)
  Method 2 — Regional OCR: four corners + bottom-center strip + top-center strip
              (AI tools place watermarks in different spots)
  Method 3 — Gemini 4-color HSV fingerprint (no Tesseract needed)
  Method 4 — Generic corner / edge overlay contrast detector
  Method 5 — White/grey semi-transparent overlay scan
              (catches light watermarks invisible to basic OCR)
"""
